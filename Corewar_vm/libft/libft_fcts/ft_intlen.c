#include "libft.h"

int		ft_intlen(int n)
{
	int	len;
	int	tmp;

	len = 1;
	tmp = n;
	while (tmp / 10 >= 1)
	{
		tmp = tmp / 10;
		len++;
	}
	return (len);
}
