#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n > -2147483648 && n <= 2147483647)
	{
		if (n < 0)
		{
			n = -n;
			ft_putchar_fd('-', fd);
		}
		if (n > 9)
		{
			ft_putnbr_fd((n / 10), fd);
			ft_putnbr_fd((n % 10), fd);
		}
		else
		{
			n = n + '0';
			ft_putchar_fd(n, fd);
		}
	}
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
}
