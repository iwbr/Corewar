int		ft_int_size(int n)
{
	int i;

	i = 1;
	if (n > -2147483648 && n < 2147483647)
	{
		if (n < 0)
		{
			n = -n;
			i++;
		}
		while (n > 9)
		{
			n = n / 10;
			i++;
		}
	}
	else if (n == -2147483648)
		i = 11;
	else if (n == 2147483647)
		i = 10;
	else
		return (0);
	return (i);
}
